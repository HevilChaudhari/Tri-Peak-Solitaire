using UnityEngine;

public class BoxTest : MonoBehaviour
{
    //----------------------------------------------------------------------------------------------------------------------------------------------------------------//
    //----------------------------------------------------------------------------------------------------------------------------------------------------------------//

    [SerializeField]private Vector2 boxSize = new Vector2(1, 1);

    //----------------------------------------------------------------------------------------------------------------------------------------------------------------//
    //----------------------------------------------------------------------------------------------------------------------------------------------------------------//

    [SerializeField]private LayerMask layerMask;

    //----------------------------------------------------------------------------------------------------------------------------------------------------------------//
    //----------------------------------------------------------------------------------------------------------------------------------------------------------------//

    public bool CheckCardOverlap()
    {
        //Local Variable to make box  trigger collider
        Vector2 boxCenter = transform.position;
        Collider2D[] colliders = Physics2D.OverlapBoxAll(boxCenter, boxSize, 0, layerMask);
        //

        //---------------------------------------------------------

        if (colliders.Length > 0)
        {
            return false;
        }
        else
        {
            return true;
        }

        //---------------------------------------------------------
    }

    //----------------------------------------------------------------------------------------------------------------------------------------------------------------//
    //----------------------------------------------------------------------------------------------------------------------------------------------------------------//
    // Visualize the box in the Scene view
    void OnDrawGizmosSelected()
    {
        Gizmos.color = Color.red;
        Gizmos.DrawWireCube(transform.position, boxSize);
    }
}
